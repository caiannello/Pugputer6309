;------------------------------------------------------------------------------
;
; ITOA64_ITOA64 - 64-BIT SIGNED INTEGER <--> DECOMAL STRING - EXPERIMENTS
;
; THESE ARE FAIRLY NAIVE 6309 IMPLEMENTATIONS OF ITOA AND ATOI FOR 64-BIT INTS
;
; A TIMED TEST HARNESS IS INCLUDED, WHICH GETS THE START AND END TIMES OF 
; THE TEST RUNS USING THE PUGPUTER 6309'S INTERRUPT-BASED 16HZ TICK COUNTER,
; AND CONVERTING 2000 INT64'S TO STRINGS AND BACK, WITH VERIFICATION.
;
; IN 6309 NATIVE MODE, WITH A BUS CLOCK OF 3.58MHZ, 
; THIS TEST CURRENTLY TAKES 223 TICKS (13.9 SECS)
; 
;------------------------------------------------------------------------------
    INCLUDE bios_func_tab.d     ; BIOS functions jump table
;------------------------------------------------------------------------------
LF  EQU  $0A        ; LINE FEED
CR  EQU  $0D        ; CARRIAGE RETURN
NUL EQU  $00
ESC EQU  $1B
;------------------------------------------------------------------------------
; ENTRYPOINT
;------------------------------------------------------------------------------
    ORG $2000
;------------------------------------------------------------------------------
START:
    ;JSR  TICKSLOOP    
    JSR TEST
    RTS
;------------------------------------------------------------------------------
; CONSTS
;------------------------------------------------------------------------------
MSG_START   FCB  LF,CR
            FCC  "RUNNING BENCHMARK ITOA64 <-> ATOI64... "
            FCB  0
MSG_TOOK    FCC  "DONE. ("
            FCB  0
MSG_END     FCC  " TICKS)"
MSG_CR      FCB  LF,CR,LF,CR,0
;------------------------------------------------------------------------------
; VARS
;------------------------------------------------------------------------------
; USED BY TESTS
SERBUF      RMB 32
STARTTICKS  RMB 8
ENDTICKS    RMB 8
; USED BY ITOA64, ATOI64
I64         RMB 8       ; WORKING VALUE
STASHED_I64 RMB 8
DIGIT       RMB 1       ; WORKING DIGIT
NDIGITS     RMB 1
LEADZ       RMB 1       ; 1:IGNORING LEADING ZEROES
ISNEG       RMB 1       ; 1:NEGATIVE NUM
;------------------------------------------------------------------------------
; TEST - TIMES 2000 CONVERSIONS FROM INT64 TO TO DECIMAL STRING AND BACK, WHILE
; VERIFYING THE RESULTS.
;------------------------------------------------------------------------------
TEST:
    LDY  #MSG_START
    JSR  BF_UT_PUTS
    JSR  BF_UT_WAITTX
    LDX  #STARTTICKS    ; GET STARTING SYS TICK COUNT (U64 16THS OF SECONDS)
    JSR  BF_RTC_GETTIX  
    LDX  #TEST_VECTORS
TESTLOOP:
    LDQ  0,X            ; STASH THE ORIGINAL I64 VALUE FOR LATER VERIFICATION.
    STQ  STASHED_I64+0
    LDQ  4,X
    STQ  STASHED_I64+4
    LDY  #SERBUF
    JSR  ITOA64         ; CONVERT I64 TO STRING,
    ;JSR  PRINTY         ; SHOW STRING RESULT
    PSHS X
    LDX  #I64
    LDY  #SERBUF        ; CONVERT STRING BACK TO I64,
    JSR  ATOI64    
    ;JSR  PRINT_U64X     ; SHOW BINARY RESULT
    LDX  #I64           ; COMPARE RESULT WITH STASHED ORIGINAL
    LDU  #STASHED_I64
    JSR  CMP64_XU
    BEQ  MATCHED
MISMATCH
    LDX  #STASHED_I64
    JSR  PRINT_U64X
    LDX  #I64
    JSR  PRINT_U64X
MATCHED
    PULS X
    CMPX #END_TEST_VECTORS
    BNE  TESTLOOP       ; IF HAVE MORE VALUES TO TEST, KEEP LOOPING,
    LDX  #ENDTICKS      ; ELSE GET ENDING SYS TICKCOUNT,
    JSR  BF_RTC_GETTIX
    LDX  #ENDTICKS
    LDU  #STARTTICKS    ; CALC ELAPSED TICKS,
    JSR  SUB64_XU
    LDY  #MSG_TOOK
    JSR  BF_UT_PUTS
    LDY  #SERBUF        ; SHOW TEST DURATION IN TICKS,
    JSR  ITOA64
    LDY  #SERBUF
    JSR  BF_UT_PUTS
    LDY  #MSG_END
    JSR  BF_UT_PUTS
    JSR  BF_UT_WAITTX
    RTS                 ; RETURN TO CALLER
;------------------------------------------------------------------------------
; SHOW SYS TICKCOUNT AS DECIMAL UNTIL ESC PRESSED.
;------------------------------------------------------------------------------
TICKSLOOP:
    JSR  BF_UT_GETC ; QUIT LOOP IF ESC RECEIVED
    CMPA #ESC
    BEQ  ELOOP
    LDX  #ENDTICKS
    JSR  BF_RTC_GETTIX ; GET LATEST TICKCOUNT.
    LDA  -1,X       ; NOTE LOWEST BYTE OF IT, AND
    CMPA STARTTICKS ; IF TICKCOUNT HASNT CHANGED,
    BEQ  TICKSLOOP  ; TRY AGAIN. ELSE,
    STA  STARTTICKS ; STORE LSB OF NEW TICKCOUNT, 
    LDX  #ENDTICKS
    LDY  #SERBUF
    JSR  ITOA64     ; AND PRINT NEW TICKCOUNT.
    JSR  PRINTY
    BRA  TICKSLOOP
ELOOP:
    RTS    
;------------------------------------------------------------------------------
; PRINT STRING AT Y + CR/LF+NULL
;------------------------------------------------------------------------------
PRINTY:
    LDA  #LF
    STA  ,Y+
    LDA  #CR
    STA  ,Y+
    LDA  #NUL
    STA  ,Y+
    LDY  #SERBUF
    JSR  BF_UT_PUTS
    JSR  BF_UT_WAITTX
    RTS
;------------------------------------------------------------------------------
; PRINT I64 AT X AS HEX + CR/LF+NULL
;------------------------------------------------------------------------------
PRINT_U64X:
    LDY  #SERBUF
    LDA  0,X
    JSR  S_HEXY
    LDA  1,X
    JSR  S_HEXY
    LDA  2,X
    JSR  S_HEXY
    LDA  3,X
    JSR  S_HEXY
    LDA  4,X
    JSR  S_HEXY
    LDA  5,X
    JSR  S_HEXY
    LDA  6,X
    JSR  S_HEXY
    LDA  7,X
    JSR  S_HEXY
    LDA  #LF
    STA  ,Y+
    LDA  #CR
    STA  ,Y+
    LDA  #NUL
    STA  ,Y+
    LDY  #SERBUF
    JSR  BF_UT_PUTS
    JSR  BF_UT_WAITTX
    RTS
;------------------------------------------------------------------------------
; CONVERTS VAL IN A TO HEX STRING AT Y. 
; DOES INCREMENT Y, BUT DOESN'T NULL-TERMINATE STRING.
;------------------------------------------------------------------------------
S_HEXY:
    TFR  A,B        ; SAVE ORIGINAL BINARY VALUE
    LSRA            ; MOVE HIGH DIGIT TO LOW DIGIT
    LSRA
    LSRA
    LSRA
    CMPA #9
    BLS  AD30       ; BRANCH IF HIGH DIGIT IS DECIMAL
    ADDA #7         ; ELSE ADD 7 SO AFTER ADDING 'O' THE
                    ; CHARACTER WILL BE IN ‘'A'..'F'
AD30:
    ADDA #'0        ; ADD ASCII O TO MAKE A CHARACTER
    ANDB #$0F       ; MASK OFF LOW DIGIT
    CMPB #9
    BLS AD3OLD      ; BRANCH IF LOW DIGIT IS DECIMAL
    ADDB #7         ; ELSE ADD 7 SO AFTER ADDING 'O! THE
                    ; CHARACTER WILL BE IN '‘A'..'F!
AD3OLD:     
    ADDB #'0        ; ADD ASCII O TO MAKE A CHARACTER
    STA ,Y+         ; INSERT HEX BYTES INTO DEST STRING AT X
    STB ,Y+         ; AND NCREMENT X
    RTS 
;------------------------------------------------------------------------------
; GIVEN AN I64 AT X, NEGATE IT IN PLACE BY SUBTRACTING IT FROM ZERO.
;------------------------------------------------------------------------------
NEGATE64:
    LDD  #0
    SUBD 6,X
    STD  6,X
    LDD  #0
    SBCD 4,X
    STD  4,X
    LDD  #0
    SBCD 2,X
    STD  2,X
    LDD  #0
    SBCD 0,X
    STD  0,X
    RTS
;------------------------------------------------------------------------------
; GIVEN I64'S AT X AND U, SUBTRACT U FROM X, LEAVING RESULT IN X.
;------------------------------------------------------------------------------
SUB64_XU:
    LDD  6,X
    SUBD 6,U
    STD  6,X
    LDD  4,X
    SBCD 4,U
    STD  4,X
    LDD  2,X
    SBCD 2,U
    STD  2,X
    LDD  0,X
    SBCD 0,U
    STD  0,X
    RTS
;------------------------------------------------------------------------------
; GIVEN I64'S AT X AND U, COMPARE X TO U.
;------------------------------------------------------------------------------
CMP64_XU:
    ANDCC #$7E          ; CLEAR CARRY
    LDD  0,X
    CMPD 0,U
    BNE  DONECMP64
    LDD  2,X
    CMPD 2,U
    BNE  DONECMP64
    LDD  4,X
    CMPD 4,U
    BNE  DONECMP64
    LDD  6,X
    CMPD 6,U
DONECMP64:
    RTS      
