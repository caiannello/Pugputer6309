;------------------------------------------------------------------------------
; GIVEN AN I64 AT X, OUTPUTS IT AS A NULL-TERMINATED DECIMAL STRING AT Y.
; ON RETURN, X WILL BE PAST THE 8-BYTE INPUT, AND Y WILL POINT AT THE STR NULL.
;------------------------------------------------------------------------------
ITOA64:
    LDQ  ,X++          ; STASH I64 INPUT VALUE AT X TO TEMP WORKING VAR
    LEAX 2,X
    STQ  I64+0
    LDQ  ,X++
    LEAX 2,X
    STQ  I64+4
    PSHS X
    LDA  I64
    ANDA #$80
    BEQ  DIDNEG
    LDX  #I64           ; IF I64 IS NEGATIVE, NEGATE IT,
    JSR  NEGATE64
    LDA  #'-'           ; AND OUTPUT A MINUS SIGN.
    STA  ,Y+
DIDNEG:
    LDX  #I64           ; X AT TMP64
    LDU  #POWERS        ; U AT POWERS TABLE
    LDA  #1
    STA  LEADZ          ; IGNORING LEADING ZEROES
IT64_LOOP:              ; FOR EACH POWER OF TEN (EACH DIGIT)
    LDA  #'0'
    STA  DIGIT
IT64_ILOOP:             ; CALCULATE A DIGIT
    JSR  CMP64_XU       ; WHILE TMP64 >= POWER:
    BLO  DONE_ILOOP
    INC  DIGIT          ;     DIGIT++
    JSR  SUB64_XU       ;     TMP64 -= POWER
    BRA  IT64_ILOOP
DONE_ILOOP:
    LDA  LEADZ          ; IF NOT IGNORING LEADING ZEROES,
    BEQ  ADDDIGIT       ; OUTPUT EVERY DIGIT.   
    LDA  DIGIT
    CMPA #'0'
    BNE  NONZERO        ; DIGIT IS NONZERO.
    CMPU #POWERS+144    ; IF ALL ZEROES, DO OUTPUT THE LAST ONE.
    BEQ  NONZERO
    BRA  SKIPDIGIT      ; SKIP THIS ZERO DIGIT.
NONZERO:
    CLR  LEADZ          ; DONE IGNORING ZEROES.
ADDDIGIT:
    LDA  DIGIT          ; ADD DIGIT TO OUTPUT
    STA  ,Y+
SKIPDIGIT:
    LEAU 8,U            ; POINT U AT NEXT (LOWER) POWER OF 10, IF ANY.
    CMPU #POWERS+152    ; IF PLACE VALUES REMAINING,
    BNE  IT64_LOOP      ; DO NEXT ONE.
IT64_DONE:
    LDA  #NUL
    STA  ,Y
    PULS X
    RTS
;------------------------------------------------------------------------------
; GIVEN A NULL-TERMINATED DECIMAL STRING AT Y, OUTPUTS AN 8-BYTE I64 AT X.
; IF INPUT IS MALFORMED, RETURNS SMALLEST POSSIBLE INT64 IN X.
; ON RETURN, X AND Y WILL BE UNCHANGED.
;------------------------------------------------------------------------------
ATOI64:
    PSHS X,Y    
    ; VALIDATE INPUT AND GET ITS LENGTH, AND BAIL OUT IF INVALID.
    LDB  #0             ; BYTE COUNT
    STB  ISNEG
ATVAL:                  ; VALIDATION LOOP:
    LDA  ,Y             ; GET NEXT CHAR OF STRING.
    TSTA
    BEQ  ATVAL_END      ; IF NULL, WE'RE AT END OF STRING.
    CMPA #'-'           ; IF NEGATIVE SIGN.
    BEQ  ATNEG
    CMPA #'0'
    BLO  ATVAL_END      ; IF OTHER NON-DIGIT WE'RE AT THE END OF STRING.
    CMPA #'9'
    BHI  ATVAL_END
    INCB                ; COUNT DECIMAL DIGITS,
    CMPB #22
    BHI  AT64_FAIL      ; IF COUNT EXCEEDS 22, FAIL.
    LEAY 1,Y            ; IDX TO NEXT CHAR OF STRING,
    BRA  ATVAL          ; AND KEEP LOOPING.
ATNEG:
    LDA  ISNEG          ; IF WE'RE SEEING REPEATED NEGATIVE SIGNS,
    BNE  AT64_FAIL      ; THEN FAIL.
    LDA  #1
    STA  ISNEG          ; NOTE IS NEGATIVE NUM,
    LEAY 1,Y            ; IDX TO NEXT CHAR OF STRING,
    BRA  ATVAL          ; AND KEEP LOOPING.
ATVAL_END:
    TSTB
    BEQ  AT64_FAIL      ; IF NO DIGITS, FAIL.
    STB  NDIGITS
    ; CLEAR 8 BYTES AT X
    LDQ  #0
    STQ  0,X
    STQ  4,X    
    ; IF INPUT IS NEGATIVE, POINT U AT THE THE NEGATIVE POWERS TABLE,
    ; ELSE POUNT U AT POSITIVE POWERS TABLE 
    LDA  ISNEG
    BNE  NEGPWRS
DOPOSPWRS:
    LDU  #NEGPOWERS+144
    BRA  ATSTART
NEGPWRS:    
    LDU  #POWERS+144
    ; LOOP BACKWARDS THROUGH INPUT STRING, STARTING AT THE ONES' PLACE,
    ; AND FOR EACH DIGIT, MULTIPLY BY THE POWER AT U AND SUM IN RESULT.
    ; THEN DECREMENT STRING POS BY ONE AND DECREMENT U BY EIGHT.
ATSTART:
    LDB  NDIGITS
    LEAY -1,Y           ; REFERENCE CHAR AT ONES PLACE    
ATDIGLOOP:
    LDA  ,Y             ; GET DIGIT
    DEC  NDIGITS        ; DECREMENT DIGITCOUNT
    SUBA #'0'           ; CONVERT DIGIT TO DECIMAL
    BEQ  ATZERO
    STA  DIGIT    
ATILOOP:    
    JSR  SUB64_XU
    DEC  DIGIT
    LDA  DIGIT
    BNE  ATILOOP
ATZERO:
    LEAY -1,Y           ; REF NEXT PLACE VAL IN STRING
    LEAU -8,U           ; AND NEXT PLACE VALUE IN POWERS TABLE
    LDB  NDIGITS
    BNE  ATDIGLOOP      ; LOOP FOR REMAINING PLACE VALUES
AT64_DONE:
    PULS X,Y
    RTS
AT64_FAIL:
    PULS X,Y
    JSR SET_MINI64_X
    RTS
;------------------------------------------------------------------------------
; WRITES INT64_MIN TO ADRESS IN X
;------------------------------------------------------------------------------
SET_MINI64_X
    LDQ  #NEGPOWERS+0
    STQ  0,X
    LDQ  #NEGPOWERS+4
    STQ  4,X
    RTS
